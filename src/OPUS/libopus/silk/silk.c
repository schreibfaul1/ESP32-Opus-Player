/* Decode quantization indices of excitation */

void silk_decode_pulses(ec_dec *psRangeDec,            /* I/O  Compressor data structure                   */
                        int16_t pulses[],              /* O    Excitation signal                           */
                        const int32_t signalType,      /* I    Sigtype                                     */
                        const int32_t quantOffsetType, /* I    quantOffsetType                             */
                        const int32_t frame_length     /* I    Frame length                                */
) {
    int32_t i, j, k, iter, abs_q, nLS, RateLevelIndex;
    int32_t sum_pulses[MAX_NB_SHELL_BLOCKS], nLshifts[MAX_NB_SHELL_BLOCKS];
    int16_t *pulses_ptr;
    const uint8_t *cdf_ptr;

    /*********************/
    /* Decode rate level */
    /*********************/
    RateLevelIndex = ec_dec_icdf(psRangeDec, silk_rate_levels_iCDF[signalType >> 1], 8);

    /* Calculate number of shell blocks */
    assert(1 << LOG2_SHELL_CODEC_FRAME_LENGTH == SHELL_CODEC_FRAME_LENGTH);
    iter = silk_RSHIFT(frame_length, LOG2_SHELL_CODEC_FRAME_LENGTH);
    if (iter * SHELL_CODEC_FRAME_LENGTH < frame_length) {
        assert(frame_length == 12 * 10); /* Make sure only happens for 10 ms @ 12 kHz */
        iter++;
    }

    /***************************************************/
    /* Sum-Weighted-Pulses Decoding                    */
    /***************************************************/
    cdf_ptr = silk_pulses_per_block_iCDF[RateLevelIndex];
    for (i = 0; i < iter; i++) {
        nLshifts[i] = 0;
        sum_pulses[i] = ec_dec_icdf(psRangeDec, cdf_ptr, 8);

        /* LSB indication */
        while (sum_pulses[i] == SILK_MAX_PULSES + 1) {
            nLshifts[i]++;
            /* When we've already got 10 LSBs, we shift the table to not allow (SILK_MAX_PULSES + 1) */
            sum_pulses[i] =
                ec_dec_icdf(psRangeDec, silk_pulses_per_block_iCDF[N_RATE_LEVELS - 1] + (nLshifts[i] == 10), 8);
        }
    }

    /***************************************************/
    /* Shell decoding                                  */
    /***************************************************/
    for (i = 0; i < iter; i++) {
        if (sum_pulses[i] > 0) {
            silk_shell_decoder(&pulses[silk_SMULBB(i, SHELL_CODEC_FRAME_LENGTH)], psRangeDec, sum_pulses[i]);
        } else {
            silk_memset(&pulses[silk_SMULBB(i, SHELL_CODEC_FRAME_LENGTH)], 0,
                        SHELL_CODEC_FRAME_LENGTH * sizeof(pulses[0]));
        }
    }

    /***************************************************/
    /* LSB Decoding                                    */
    /***************************************************/
    for (i = 0; i < iter; i++) {
        if (nLshifts[i] > 0) {
            nLS = nLshifts[i];
            pulses_ptr = &pulses[silk_SMULBB(i, SHELL_CODEC_FRAME_LENGTH)];
            for (k = 0; k < SHELL_CODEC_FRAME_LENGTH; k++) {
                abs_q = pulses_ptr[k];
                for (j = 0; j < nLS; j++) {
                    abs_q = silk_LSHIFT(abs_q, 1);
                    abs_q += ec_dec_icdf(psRangeDec, silk_lsb_iCDF, 8);
                }
                pulses_ptr[k] = abs_q;
            }
            /* Mark the number of pulses non-zero for sign decoding. */
            sum_pulses[i] |= nLS << 5;
        }
    }

    /****************************************/
    /* Decode and add signs to pulse signal */
    /****************************************/
    silk_decode_signs(psRangeDec, pulses, frame_length, signalType, quantOffsetType, sum_pulses);
}
