/* Decode side-information parameters from payload */
void silk_decode_indices(silk_decoder_state *psDec, /* I/O  State                                       */
                         ec_dec *psRangeDec,        /* I/O  Compressor data structure                   */
                         int32_t FrameIndex,        /* I    Frame number                                */
                         int32_t decode_LBRR,       /* I    Flag indicating LBRR data is being decoded  */
                         int32_t condCoding         /* I    The type of conditional coding to use       */
) {
    int32_t i, k, Ix;
    int32_t decode_absolute_lagIndex, delta_lagIndex;
    int16_t ec_ix[MAX_LPC_ORDER];
    uint8_t pred_Q8[MAX_LPC_ORDER];

    /*******************************************/
    /* Decode signal type and quantizer offset */
    /*******************************************/
    if (decode_LBRR || psDec->VAD_flags[FrameIndex]) {
        Ix = ec_dec_icdf(psRangeDec, silk_type_offset_VAD_iCDF, 8) + 2;
    } else {
        Ix = ec_dec_icdf(psRangeDec, silk_type_offset_no_VAD_iCDF, 8);
    }
    psDec->indices.signalType = (int8_t)silk_RSHIFT(Ix, 1);
    psDec->indices.quantOffsetType = (int8_t)(Ix & 1);

    /****************/
    /* Decode gains */
    /****************/
    /* First subframe */
    if (condCoding == CODE_CONDITIONALLY) {
        /* Conditional coding */
        psDec->indices.GainsIndices[0] = (int8_t)ec_dec_icdf(psRangeDec, silk_delta_gain_iCDF, 8);
    } else {
        /* Independent coding, in two stages: MSB bits followed by 3 LSBs */
        psDec->indices.GainsIndices[0] =
            (int8_t)silk_LSHIFT(ec_dec_icdf(psRangeDec, silk_gain_iCDF[psDec->indices.signalType], 8), 3);
        psDec->indices.GainsIndices[0] += (int8_t)ec_dec_icdf(psRangeDec, silk_uniform8_iCDF, 8);
    }

    /* Remaining subframes */
    for (i = 1; i < psDec->nb_subfr; i++) {
        psDec->indices.GainsIndices[i] = (int8_t)ec_dec_icdf(psRangeDec, silk_delta_gain_iCDF, 8);
    }

    /**********************/
    /* Decode LSF Indices */
    /**********************/
    psDec->indices.NLSFIndices[0] = (int8_t)ec_dec_icdf(
        psRangeDec, &psDec->psNLSF_CB->CB1_iCDF[(psDec->indices.signalType >> 1) * psDec->psNLSF_CB->nVectors], 8);
    silk_NLSF_unpack(ec_ix, pred_Q8, psDec->psNLSF_CB, psDec->indices.NLSFIndices[0]);
    assert(psDec->psNLSF_CB->order == psDec->LPC_order);
    for (i = 0; i < psDec->psNLSF_CB->order; i++) {
        Ix = ec_dec_icdf(psRangeDec, &psDec->psNLSF_CB->ec_iCDF[ec_ix[i]], 8);
        if (Ix == 0) {
            Ix -= ec_dec_icdf(psRangeDec, silk_NLSF_EXT_iCDF, 8);
        } else if (Ix == 2 * NLSF_QUANT_MAX_AMPLITUDE) {
            Ix += ec_dec_icdf(psRangeDec, silk_NLSF_EXT_iCDF, 8);
        }
        psDec->indices.NLSFIndices[i + 1] = (int8_t)(Ix - NLSF_QUANT_MAX_AMPLITUDE);
    }

    /* Decode LSF interpolation factor */
    if (psDec->nb_subfr == MAX_NB_SUBFR) {
        psDec->indices.NLSFInterpCoef_Q2 = (int8_t)ec_dec_icdf(psRangeDec, silk_NLSF_interpolation_factor_iCDF, 8);
    } else {
        psDec->indices.NLSFInterpCoef_Q2 = 4;
    }

    if (psDec->indices.signalType == TYPE_VOICED) {
        /*********************/
        /* Decode pitch lags */
        /*********************/
        /* Get lag index */
        decode_absolute_lagIndex = 1;
        if (condCoding == CODE_CONDITIONALLY && psDec->ec_prevSignalType == TYPE_VOICED) {
            /* Decode Delta index */
            delta_lagIndex = (int16_t)ec_dec_icdf(psRangeDec, silk_pitch_delta_iCDF, 8);
            if (delta_lagIndex > 0) {
                delta_lagIndex = delta_lagIndex - 9;
                psDec->indices.lagIndex = (int16_t)(psDec->ec_prevLagIndex + delta_lagIndex);
                decode_absolute_lagIndex = 0;
            }
        }
        if (decode_absolute_lagIndex) {
            /* Absolute decoding */
            psDec->indices.lagIndex =
                (int16_t)ec_dec_icdf(psRangeDec, silk_pitch_lag_iCDF, 8) * silk_RSHIFT(psDec->fs_kHz, 1);
            psDec->indices.lagIndex += (int16_t)ec_dec_icdf(psRangeDec, psDec->pitch_lag_low_bits_iCDF, 8);
        }
        psDec->ec_prevLagIndex = psDec->indices.lagIndex;

        /* Get countour index */
        psDec->indices.contourIndex = (int8_t)ec_dec_icdf(psRangeDec, psDec->pitch_contour_iCDF, 8);

        /********************/
        /* Decode LTP gains */
        /********************/
        /* Decode PERIndex value */
        psDec->indices.PERIndex = (int8_t)ec_dec_icdf(psRangeDec, silk_LTP_per_index_iCDF, 8);

        for (k = 0; k < psDec->nb_subfr; k++) {
            psDec->indices.LTPIndex[k] =
                (int8_t)ec_dec_icdf(psRangeDec, silk_LTP_gain_iCDF_ptrs[psDec->indices.PERIndex], 8);
        }

        /**********************/
        /* Decode LTP scaling */
        /**********************/
        if (condCoding == CODE_INDEPENDENTLY) {
            psDec->indices.LTP_scaleIndex = (int8_t)ec_dec_icdf(psRangeDec, silk_LTPscale_iCDF, 8);
        } else {
            psDec->indices.LTP_scaleIndex = 0;
        }
    }
    psDec->ec_prevSignalType = psDec->indices.signalType;

    /***************/
    /* Decode seed */
    /***************/
    psDec->indices.Seed = (int8_t)ec_dec_icdf(psRangeDec, silk_uniform4_iCDF, 8);
}
//----------------------------------------------------------------------------------------------------------------------

void silk_decode_parameters(silk_decoder_state *psDec,       /* I/O  State                                       */
                            silk_decoder_control *psDecCtrl, /* I/O  Decoder control                             */
                            int32_t condCoding               /* I    The type of conditional coding to use       */
) {
    int32_t i, k, Ix;
    int16_t pNLSF_Q15[MAX_LPC_ORDER], pNLSF0_Q15[MAX_LPC_ORDER];
    const int8_t *cbk_ptr_Q7;

    /* Dequant Gains */
    silk_gains_dequant(psDecCtrl->Gains_Q16, psDec->indices.GainsIndices, &psDec->LastGainIndex,
                       condCoding == CODE_CONDITIONALLY, psDec->nb_subfr);

    /****************/
    /* Decode NLSFs */
    /****************/
    silk_NLSF_decode(pNLSF_Q15, psDec->indices.NLSFIndices, psDec->psNLSF_CB);

    /* Convert NLSF parameters to AR prediction filter coefficients */
    silk_NLSF2A(psDecCtrl->PredCoef_Q12[1], pNLSF_Q15, psDec->LPC_order, psDec->arch);

    /* If just reset, e.g., because internal Fs changed, do not allow interpolation */
    /* improves the case of packet loss in the first frame after a switch           */
    if (psDec->first_frame_after_reset == 1) {
        psDec->indices.NLSFInterpCoef_Q2 = 4;
    }

    if (psDec->indices.NLSFInterpCoef_Q2 < 4) {
        /* Calculation of the interpolated NLSF0 vector from the interpolation factor, */
        /* the previous NLSF1, and the current NLSF1                                   */
        for (i = 0; i < psDec->LPC_order; i++) {
            pNLSF0_Q15[i] =
                psDec->prevNLSF_Q15[i] +
                silk_RSHIFT(silk_MUL(psDec->indices.NLSFInterpCoef_Q2, pNLSF_Q15[i] - psDec->prevNLSF_Q15[i]), 2);
        }

        /* Convert NLSF parameters to AR prediction filter coefficients */
        silk_NLSF2A(psDecCtrl->PredCoef_Q12[0], pNLSF0_Q15, psDec->LPC_order, psDec->arch);
    } else {
        /* Copy LPC coefficients for first half from second half */
        silk_memcpy(psDecCtrl->PredCoef_Q12[0], psDecCtrl->PredCoef_Q12[1], psDec->LPC_order * sizeof(int16_t));
    }

    silk_memcpy(psDec->prevNLSF_Q15, pNLSF_Q15, psDec->LPC_order * sizeof(int16_t));

    /* After a packet loss do BWE of LPC coefs */
    if (psDec->lossCnt) {
        silk_bwexpander(psDecCtrl->PredCoef_Q12[0], psDec->LPC_order, BWE_AFTER_LOSS_Q16);
        silk_bwexpander(psDecCtrl->PredCoef_Q12[1], psDec->LPC_order, BWE_AFTER_LOSS_Q16);
    }

    if (psDec->indices.signalType == TYPE_VOICED) {
        /*********************/
        /* Decode pitch lags */
        /*********************/

        /* Decode pitch values */
        silk_decode_pitch(psDec->indices.lagIndex, psDec->indices.contourIndex, psDecCtrl->pitchL, psDec->fs_kHz,
                          psDec->nb_subfr);

        /* Decode Codebook Index */
        cbk_ptr_Q7 = silk_LTP_vq_ptrs_Q7[psDec->indices.PERIndex]; /* set pointer to start of codebook */

        for (k = 0; k < psDec->nb_subfr; k++) {
            Ix = psDec->indices.LTPIndex[k];
            for (i = 0; i < LTP_ORDER; i++) {
                psDecCtrl->LTPCoef_Q14[k * LTP_ORDER + i] = silk_LSHIFT(cbk_ptr_Q7[Ix * LTP_ORDER + i], 7);
            }
        }

        /**********************/
        /* Decode LTP scaling */
        /**********************/
        Ix = psDec->indices.LTP_scaleIndex;
        psDecCtrl->LTP_scale_Q14 = silk_LTPScales_table_Q14[Ix];
    } else {
        silk_memset(psDecCtrl->pitchL, 0, psDec->nb_subfr * sizeof(int32_t));
        silk_memset(psDecCtrl->LTPCoef_Q14, 0, LTP_ORDER * psDec->nb_subfr * sizeof(int16_t));
        psDec->indices.PERIndex = 0;
        psDecCtrl->LTP_scale_Q14 = 0;
    }
}
